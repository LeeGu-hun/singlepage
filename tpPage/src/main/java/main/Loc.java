package main;

public class Loc {
	private String sido, gungu;

	public Loc(String sido, String gungu) {
		this.sido = sido;
		this.gungu = gungu;
	}

	public String getSido() {
		return sido;
	}

	public void setSido(String sido) {
		this.sido = sido;
	}

	public String getGungu() {
		return gungu;
	}

	public void setGungu(String gungu) {
		this.gungu = gungu;
	}
}
